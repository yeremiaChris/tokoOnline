const rootReducer = (state = initState, action) => {
  switch (action.type) {
    case "addCart":
      return {
        // "spread" the original state object
        ...state,
        cart: state.cart.some((item) => item.key === action.item.key)
          ? [
              ...state.cart.map((item) =>
                item.key === action.item.key
                  ? { ...item, quantity: item.quantity + 1 }
                  : item
              ),
            ]
          : [...state.cart, action.item],
        // but replace the "chosenIds" field
      };
    case "incrementItem":
      return {
        ...state,
        cart: [
          ...state.cart.map((item) =>
            item.key === action.key
              ? { ...item, quantity: item.quantity + 1 }
              : item
          ),
        ],
      };
    case "decrementItem":
      return {
        ...state,
        cart: [
          ...state.cart.map((item) =>
            item.key === action.key && item.quantity > 0
              ? { ...item, quantity: item.quantity - 1 }
              : item
          ),
        ],
      };
    case "deleteCart":
      return {
        ...state,
        cart: [...state.cart.filter((item) => item.key !== action.key)],
      };
    case "addItem":
      return {
        ...state,
        item: [...state.item, action.item],
      };
    default:
      return state;
  }
};

export default rootReducer;
